import java.util.*;

public class basicArraySyntax {

    public static void main(String[] args) {
        int[] marks = new int[3];
        
        marks[0] = 97;
        marks[1] = 96;
        marks[2] = 98;

        System.out.println("Math: " + marks[0]);
        System.out.println("Java: " + marks[1]);
        System.out.println("Python: " + marks[2]);
    }
}
